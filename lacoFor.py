# Laço for
# O laço for é usado para iterar sobre uma sequência (como uma lista, tupla, conjunto ou string) ou outros objetos iteráveis.
# O laço for executa um bloco de código para cada item na sequência.
# O laço for é útil quando você precisa processar cada item em uma lista ou outra
# sequência de forma individual.
# Sintaxe:
# for variável in sequência:
#     # bloco de código a ser executado para cada item na sequência
# Exemplo:
for i in range(100):
    print(i)  # imprime os números de 0 a 4