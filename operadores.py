
# Aritméticos

# Soma
print(2 + 3)
# Saída: 5
# Subtração
print(2 - 3)
# Saída: -1
# Multiplicação
print(2 * 3)
# Saída: 6
# Divisão
print(2 / 3)
# Saída: 0.6666666666666666
# Divisão inteira
print(2 // 3)
# Saída: 0
# Resto da divisão
print(2 % 3)
# Saída: 2
# Potência
print(2 ** 3)
