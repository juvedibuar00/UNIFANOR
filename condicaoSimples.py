# Uso de condicional simples
# Si la edad es mayor a 18, entonces es mayor de edad
idade = int(input('Digite sua idade: '))
# A função input() é usada para receber dados do usuário.
if idade > 18:
    print('Você é maior de idade')
else:
    print('Você é menor de idade')
# A instrução if é usada para verificar se a idade é maior que 18.
# Se a idade for maior que 18, a mensagem "Você é maior de idade" será exibida.

