# Use de input
nome = input("Digite seu nome: ")
# A função input() é usada para receber dados do usuário.
# O texto "Digite seu nome: " é exibido na tela.
# O valor digitado pelo usuário é armazenado na variável nome.
print(f'Prazer em te conhcer {nome}. Seja bem-vindo!')
# A função print() exibe a mensagem "Prazer em te conhecer {nome}. Seja bem-vindo!" na tela.
